public class Escape {
    public static void main(String[] args) {
        //Escape sequences
        /*  \n->new line
        /t->tab space
        \"->Prints the double qoute
        \'->Prints the single quote
        \b->puts a backspace
        */
        System.out.println("Hi \'Ella\'");
        System.out.println("Hi \"Ella\"");
        System.out.println("Hi \nElla");
        System.out.println("Hi \bElla");
        System.out.println("Hi \tElla");


    }
}
